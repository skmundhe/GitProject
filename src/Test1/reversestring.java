package Test1;

public class reversestring {

	
	
	
	public static void main(String[] args) {      
		
		for (int i=6; i<=6;i++);{
			for(int j=2; j<=2;j++);
			{
				System.out.println("9");
		}
			System.out.println(" ");
	}
		for (int i=5; i<=5; i--) {
		for(int j=2;j<=2;j--);{
			System.out.println(9);
		}
		System.out.print(" ");
	}}
}